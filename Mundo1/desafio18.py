import math
n1=float(input('Digite um numero: '))
seno=math.sin(math.radians(n1))
print(f'o angulo de {n1} tem o seno de {seno:.2f}')
cos=math.cos(math.radians(n1))
print(f'o angulo de {n1} tem o cos de {cos:.2f}')
tan=math.tan(math.radians(n1))
print(f'o angulo de {n1} tem a tan de {tan:.2f}')
